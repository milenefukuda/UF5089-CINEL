primeiroValor = int(input("Escreva um valor inteiro: "))
segundoValor = int(input("Escreva um segundo valor inteiro: "))

soma = primeiroValor + segundoValor
dif = primeiroValor - segundoValor
produto = primeiroValor * segundoValor
quociente = primeiroValor / segundoValor
resto = primeiroValor % segundoValor

print(f"A soma é {soma}. A diferença é {dif}. O produto é {produto}. O quociente é {quociente}. O módulo é {resto}")